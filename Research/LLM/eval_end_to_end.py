"""
End-to-end eval — simulates the full REFLECT runtime flow:

  1. A journal entry is uploaded
  2. Relevant chunks are retrieved from the vector store (RAG)
  3. Retrieved context is passed into the /generate-question prompt
  4. The generated question is judged for specificity, depth, rule following
  5. The answer grounded in retrieved context is judged for faithfulness

This is the most realistic test: it catches cases where a model follows
the prompt fine in isolation but breaks when the context comes from retrieval.
"""

import os, json
import pandas as pd
from llama_index.core import SimpleDirectoryReader, VectorStoreIndex
from llama_index.llms.ollama import Ollama
from llama_index.embeddings.ollama import OllamaEmbedding
from llama_index.core import Settings

# ── config ────────────────────────────────────────────────────────────────────
OLLAMA_BASE_URL = "http://localhost:11434"
EVAL_DATA_PATH  = "./eval_data"
RESULTS_PATH    = "./eval_results"
EMBED_MODEL     = "nomic-embed-text"
TOP_K           = 3

MODELS_TO_TEST  = [
    "mistral",
    "llama3",
    "gpt-oss:20b",
]

# ── prompts ───────────────────────────────────────────────────────────────────
# paste your real /generate-question template here
REFLECT_PROMPT = """You are a reflective journaling assistant.
Based on the following excerpts from a user's journal, generate a single
clarifying question that helps the writer go deeper on a theme that appears
across their entries.

Rules:
- Ask about ONE specific theme from the excerpts
- Do not ask yes/no questions
- Do not start with "How does that make you feel"
- Keep it under 20 words

Journal excerpts:
{context}

Question:"""

JUDGE_PROMPT = """You are evaluating a journaling question generated by an AI assistant.
Respond only with valid JSON, no other text.

Journal excerpts used as context:
{context}

Generated question:
{question}

Rate each dimension 1-5:
- specificity: does it reference a concrete theme from the excerpts (not generic)?
- depth: would it push the writer to reflect meaningfully?
- rule_following: follows rules (not yes/no, not "how does that make you feel", under 20 words)?
- context_grounding: is the question clearly grounded in the retrieved excerpts, not hallucinated?

{{"specificity": N, "depth": N, "rule_following": N, "context_grounding": N, "reason": "one sentence"}}"""

# ── test queries ──────────────────────────────────────────────────────────────
# these simulate what a user might ask or what REFLECT triggers on
TEST_QUERIES = [
    "What recurring patterns appear in how I handle work stress?",
    "What does my relationship with productivity look like?",
    "How do I talk about my relationships with other people?",
    "What themes come up when I'm having a low energy day?",
    "What am I avoiding or procrastinating on?",
]

# ── helpers ───────────────────────────────────────────────────────────────────
def build_components(model_name: str):
    extra = {"think": False} if "qwen" in model_name.lower() else {}
    llm   = Ollama(
        model=model_name,
        base_url=OLLAMA_BASE_URL,
        request_timeout=120.0,
        additional_kwargs=extra,
    )
    embed = OllamaEmbedding(model_name=EMBED_MODEL, base_url=OLLAMA_BASE_URL)
    return llm, embed

def build_index(llm, embed) -> VectorStoreIndex:
    documents = SimpleDirectoryReader(EVAL_DATA_PATH).load_data()
    Settings.llm         = llm
    Settings.embed_model = embed
    return VectorStoreIndex.from_documents(documents)

def retrieve_context(index: VectorStoreIndex, query: str) -> tuple[str, list[str]]:
    retriever = index.as_retriever(similarity_top_k=TOP_K)
    nodes     = retriever.retrieve(query)
    chunks    = [n.get_content() for n in nodes]
    context   = "\n\n---\n\n".join(chunks)
    return context, chunks

def generate_question(llm, context: str) -> str:
    prompt = REFLECT_PROMPT.format(context=context)
    return str(llm.complete(prompt)).strip()

def judge_question(judge_llm, context: str, question: str) -> dict:
    prompt = JUDGE_PROMPT.format(context=context, question=question)
    raw    = str(judge_llm.complete(prompt)).strip()
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        start = raw.find("{")
        end   = raw.rfind("}") + 1
        try:
            return json.loads(raw[start:end]) if start != -1 else {}
        except json.JSONDecodeError:
            return {}

# ── main ──────────────────────────────────────────────────────────────────────
os.makedirs(RESULTS_PATH, exist_ok=True)

# fixed judge to avoid self-grading bias
judge_llm = Ollama(model="mistral", base_url=OLLAMA_BASE_URL, request_timeout=120.0)

rows = []

for model_name in MODELS_TO_TEST:
    print(f"\n── {model_name} ──")
    llm, embed = build_components(model_name)
    index      = build_index(llm, embed)

    for query in TEST_QUERIES:
        print(f"  query: {query[:60]}...")
        context, chunks = retrieve_context(index, query)
        question        = generate_question(llm, context)
        scores          = judge_question(judge_llm, context, question)

        print(f"  question: {question}")

        rows.append({
            "model":             model_name,
            "query":             query,
            "retrieved_chunks":  len(chunks),
            "question":          question,
            "specificity":       scores.get("specificity"),
            "depth":             scores.get("depth"),
            "rule_following":    scores.get("rule_following"),
            "context_grounding": scores.get("context_grounding"),
            "reason":            scores.get("reason"),
        })

df = pd.DataFrame(rows)

summary = df.groupby("model")[
    ["specificity", "depth", "rule_following", "context_grounding"]
].mean().round(2)

print("\n── e2e summary ──")
print(summary)

df.to_csv(f"{RESULTS_PATH}/e2e_eval.csv", index=False)
summary.to_csv(f"{RESULTS_PATH}/e2e_eval_summary.csv")
print(f"\nSaved → {RESULTS_PATH}/e2e_eval.csv")