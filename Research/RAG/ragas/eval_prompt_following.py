"""
Prompt-following eval for REFLECT question generation.

This benchmarks multiple generator models against:
1. Deterministic rule checks (word count, yes/no, banned opener).
2. One or more LLM judges for nuanced quality dimensions.
"""

import argparse
import json
import re
from pathlib import Path
from statistics import mean, pstdev
from typing import Any

import pandas as pd
from llama_index.llms.ollama import Ollama


SCRIPT_DIR = Path(__file__).resolve().parent
EVAL_DATA_PATH = SCRIPT_DIR / "eval_data"
RESULTS_PATH = SCRIPT_DIR / "eval_results"

OLLAMA_BASE_URL = "http://localhost:11434"
MODELS_TO_TEST = ["mistral", "llama3", "gpt-oss:20b"]
JUDGE_MODELS = ["mistral", "llama3"]

WORD_RE = re.compile(r"[A-Za-z0-9]+(?:['-][A-Za-z0-9]+)?")
YES_NO_STARTERS = {
    "is",
    "are",
    "am",
    "was",
    "were",
    "do",
    "does",
    "did",
    "can",
    "could",
    "would",
    "will",
    "have",
    "has",
    "had",
    "should",
    "shall",
    "may",
    "might",
    "must",
}
BANNED_OPENER = "how does that make you feel"

REFLECT_PROMPT = """You are a reflective journaling assistant.
Given the following journal entry, generate a single clarifying question
that helps the writer go deeper on something they mentioned but did not explore.

Rules:
- Ask about ONE specific thing from the entry
- Do not ask yes/no questions
- Do not start with "How does that make you feel"
- Keep it under 20 words

Journal entry:
{journal_text}

Question:"""

JUDGE_PROMPT = """You are evaluating a journaling question generated by an AI assistant.
Respond only with valid JSON, no extra text.

Journal entry:
{journal_text}

Generated question:
{question}

Rate each dimension 1-5:
- specificity: references concrete details from the entry, not generic advice.
- depth: likely to trigger meaningful self-reflection.
- grounding: clearly anchored in this entry rather than a generic journaling question.

{{"specificity": N, "depth": N, "grounding": N, "reason": "one sentence"}}"""


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Run prompt-following benchmark.")
    parser.add_argument("--models", nargs="+", default=MODELS_TO_TEST, help="Generator models to evaluate.")
    parser.add_argument("--judge-models", nargs="+", default=JUDGE_MODELS, help="Judge models for score aggregation.")
    parser.add_argument("--max-entries", type=int, default=None, help="Optional cap for quick smoke tests.")
    parser.add_argument("--request-timeout", type=float, default=120.0, help="Ollama request timeout in seconds.")
    parser.add_argument("--output-prefix", default="prompt_eval", help="Output filename prefix in eval_results.")
    return parser.parse_args()


def build_llm(model_name: str, request_timeout: float) -> Ollama:
    extra = {"think": False} if "qwen" in model_name.lower() else {}
    return Ollama(
        model=model_name,
        base_url=OLLAMA_BASE_URL,
        request_timeout=request_timeout,
        additional_kwargs=extra,
    )


def load_test_entries(max_entries: int | None) -> dict[str, str]:
    if not EVAL_DATA_PATH.exists():
        raise FileNotFoundError(f"eval_data directory not found: {EVAL_DATA_PATH}")

    files = sorted(EVAL_DATA_PATH.glob("*.txt"))
    if not files:
        raise ValueError(f"No .txt files found in: {EVAL_DATA_PATH}")

    entries: list[tuple[str, str]] = []
    for file_path in files:
        text = file_path.read_text(encoding="utf-8", errors="ignore").strip()
        if text:
            entries.append((file_path.stem, text))

    if max_entries is not None:
        entries = entries[: max(0, max_entries)]

    if not entries:
        raise ValueError("No non-empty entries available for evaluation.")

    return dict(entries)


def extract_json_dict(raw: str) -> dict[str, Any]:
    text = raw.strip()
    if text.startswith("```"):
        text = text.strip("`")
        if text.startswith("json"):
            text = text[4:]
        text = text.strip()

    try:
        parsed = json.loads(text)
        return parsed if isinstance(parsed, dict) else {}
    except json.JSONDecodeError:
        start = text.find("{")
        end = text.rfind("}") + 1
        if start == -1 or end <= start:
            return {}
        try:
            parsed = json.loads(text[start:end])
            return parsed if isinstance(parsed, dict) else {}
        except json.JSONDecodeError:
            return {}


def to_score(value: Any) -> int | None:
    try:
        score = int(value)
    except (TypeError, ValueError):
        return None
    return max(1, min(5, score))


def analyze_rules(question: str) -> dict[str, Any]:
    clean = " ".join(question.strip().strip('"').split())
    words = WORD_RE.findall(clean)
    word_count = len(words)
    first_word = words[0].lower() if words else ""

    is_yes_no = first_word in YES_NO_STARTERS
    starts_with_banned = clean.lower().startswith(BANNED_OPENER)

    violations = int(word_count > 20) + int(is_yes_no) + int(starts_with_banned)
    score = 5
    if word_count > 20:
        score -= 2
    if is_yes_no:
        score -= 2
    if starts_with_banned:
        score -= 3

    return {
        "word_count": word_count,
        "is_yes_no": is_yes_no,
        "starts_with_banned": starts_with_banned,
        "strict_rule_pass": violations == 0,
        "rule_following_det": max(1, score),
    }


def generate_question(llm: Ollama, journal_text: str) -> str:
    prompt = REFLECT_PROMPT.format(journal_text=journal_text)
    return str(llm.complete(prompt)).strip()


def judge_question(judge_llm: Ollama, journal_text: str, question: str) -> dict[str, Any]:
    prompt = JUDGE_PROMPT.format(journal_text=journal_text, question=question)
    raw = str(judge_llm.complete(prompt)).strip()
    return extract_json_dict(raw)


def aggregate_judgements(judge_payloads: dict[str, dict[str, Any]]) -> dict[str, Any]:
    metrics = ("specificity", "depth", "grounding")
    per_judge_scores: dict[str, dict[str, int | None]] = {}
    reasons: list[str] = []
    valid_count = 0

    for judge_name, payload in judge_payloads.items():
        row_scores = {metric: to_score(payload.get(metric)) for metric in metrics}
        per_judge_scores[judge_name] = row_scores

        if any(value is not None for value in row_scores.values()):
            valid_count += 1

        reason = payload.get("reason")
        if isinstance(reason, str) and reason.strip():
            reasons.append(f"{judge_name}: {reason.strip()}")

    aggregated: dict[str, Any] = {}
    for metric in metrics:
        values = [
            row[metric]
            for row in per_judge_scores.values()
            if row[metric] is not None
        ]
        aggregated[metric] = round(mean(values), 3) if values else None
        if not values:
            aggregated[f"{metric}_judge_std"] = None
        elif len(values) == 1:
            aggregated[f"{metric}_judge_std"] = 0.0
        else:
            aggregated[f"{metric}_judge_std"] = round(pstdev(values), 3)

    judge_count = max(1, len(judge_payloads))
    aggregated["judge_valid_count"] = valid_count
    aggregated["judge_valid_rate"] = round(valid_count / judge_count, 3)
    aggregated["judge_reasons"] = " | ".join(reasons)
    aggregated["judge_scores_json"] = json.dumps(per_judge_scores, ensure_ascii=True)
    return aggregated


def main() -> None:
    args = parse_args()
    RESULTS_PATH.mkdir(parents=True, exist_ok=True)

    entries = load_test_entries(args.max_entries)
    judge_llms = {
        judge_name: build_llm(judge_name, args.request_timeout)
        for judge_name in args.judge_models
    }
    rows: list[dict[str, Any]] = []

    print(f"Loaded {len(entries)} journal entries")
    print(f"Evaluating {len(args.models)} generator model(s)")
    print(f"Using {len(judge_llms)} judge model(s): {', '.join(judge_llms.keys())}")

    for model_name in args.models:
        print(f"\n-- evaluating {model_name} --")
        generator_llm = build_llm(model_name, args.request_timeout)

        for entry_type, journal_text in entries.items():
            question = generate_question(generator_llm, journal_text)
            rule_stats = analyze_rules(question)

            judge_payloads: dict[str, dict[str, Any]] = {}
            for judge_name, judge_llm in judge_llms.items():
                judge_payloads[judge_name] = judge_question(judge_llm, journal_text, question)

            aggregate = aggregate_judgements(judge_payloads)

            print(f"  [{entry_type}] {question}")

            rows.append(
                {
                    "model": model_name,
                    "entry_type": entry_type,
                    "question": question,
                    **rule_stats,
                    **aggregate,
                }
            )

    df = pd.DataFrame(rows)
    summary = (
        df.groupby("model")
        .agg(
            samples=("question", "count"),
            specificity=("specificity", "mean"),
            depth=("depth", "mean"),
            grounding=("grounding", "mean"),
            rule_following_det=("rule_following_det", "mean"),
            strict_rule_pass_rate=("strict_rule_pass", "mean"),
            judge_valid_rate=("judge_valid_rate", "mean"),
        )
        .round(3)
        .sort_values(
            ["specificity", "depth", "grounding", "rule_following_det"],
            ascending=False,
        )
    )

    per_example_path = RESULTS_PATH / f"{args.output_prefix}.csv"
    summary_path = RESULTS_PATH / f"{args.output_prefix}_summary.csv"
    df.to_csv(per_example_path, index=False)
    summary.to_csv(summary_path)

    print("\n prompt following summary ")
    print(summary)
    print(f"\nSaved -> {per_example_path}")
    print(f"Saved -> {summary_path}")


if __name__ == "__main__":
    main()